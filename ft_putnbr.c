#include "ft_printf.h"

void	ft_putnbr(int nb)
{
	char	a;

	if (nb == -2147483648)
	{
		write(1, "-", 1);
		write(1, "2147483648", 10);
	}
	else
	{
		if (nb >= 10)
			ft_putnbr(nb / 10);
		if (nb < 0)
		{
			write(1, "-", 1);
			nb = (-nb);
			if (nb >= 10)
				ft_putnbr(nb / 10);
		}
		a = (nb % 10) + '0';
		write(1, &a, 1);
	}
}
