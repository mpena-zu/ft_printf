#include "ft_printf.h"

int	ft_len_int(int n)
{
	int	len;

	len = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		n = -n;
		len++;
	}
	if (n == -2147483648)
		return (11);
	while (n > 0)
	{
		n = n / 10;
		len++;
	}
	return (len);
}
